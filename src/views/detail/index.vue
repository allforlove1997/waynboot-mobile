<template>
  <div class="detail">
    <nav-bar title="商品详情">
      <span style="color:#333">
        <svg-icon icon-class="share" :width="15" :height="15" />
      </span>
    </nav-bar>

    <Swiper :banner="banner" />

    <Overview
      :title="overview.name"
      :desc="overview.brief"
      :price="overview.retailPrice"
      :discount="overview.counterPrice"
    />

    <Section @input="isSkuShow = $event" />

    <!-- <Comment :rate="comment.rate" :num="comment.num" :tags="comment.tags" :list="comment.list" /> -->

    <Description :description="description" />

    <!-- <Sku :skudata="skudata" :goods="goods" v-model="isSkuShow" /> -->

    <Tabbar @input="isSkuShow = $event" />
    <back-top />
    <Skeleton v-if="isSkeletonShow"/>
  </div>
</template>

<script>
import { getDetail } from '@/api/detail'
import NavBar from '@/components/NavBar'
import Swiper from './modules/Swiper'
import Overview from './modules/Overview'
import Section from './modules/Section'
import Comment from './modules/Comment'
import Description from './modules/Description'
import Tabbar from './modules/Tabbar'
import Sku from './modules/Sku'
import Skeleton from './modules/Skeleton'

export default {
  name: 'Detail',
  props: ['goodsId'],
  components: {
    NavBar,
    Swiper,
    Overview,
    Section,
    Comment,
    Description,
    Tabbar,
    Sku,
    Skeleton
  },
  data() {
    return {
      banner: [],
      overview: {},
      comment: {},
      description: '',
      skudata: {},
      goods: {},
      isSkuShow: false,
      isSkeletonShow: true
    }
  },
  mounted() {
    this.getDetail()
  },
  methods: {
    getDetail() {
      getDetail({
        goodsId: this.goodsId
      }).then(res => {
        const { data: overview } = res.map
        this.overview = overview
        this.banner = overview.gallery
        this.description = overview.picUrl
        this.isSkeletonShow = false
      })
    }
  }
}
</script>

